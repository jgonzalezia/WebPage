                <div class="intro-heading">Política de Privacidad</div>
                <p>El presente documento constituye el Aviso de Privacidad para efectos de lo dispuesto en la Ley Federal de Protección de Datos Personales en Posesión de los Particulares (la “LFPDP”) y las disposiciones que emanan de ella o se relacionan con la misma. Este Aviso de privacidad aplica a la información personal recopilada sobre el Titular por IA Society S.C. de R.L.) en su carácter de Responsable. 
                Para los efectos del presente Aviso de privacidad, los términos que no estén contemplados en el mismo tendrán el significado que se les atribuye en la Ley Federal de Protección de Datos Personales en Posesión de los Particulares (LFPDP). El presente Aviso de privacidad tiene por objeto establecer términos y condiciones por medio de los cuales IA Society (o el Encargado que designe IA Society) recibirá y protegerá los Datos Personales del Titular, a efecto de proteger su privacidad; utilizará los Datos Personales del Titular, y /o realizará en su caso las transferencias sus Datos Personales a terceros.
                </p>
                <h2 class="section-heading">1. CONSENTIMIENTO DEL TITULAR.</h2>
                <p>
                Para efectos de lo dispuesto en la LFPDP y en particular en su Artículo 17, el Titular manifiesta que: 
                1.1- El presente Aviso de privacidad le ha sido dado a conocer por el Responsable.
                1.2- Que ha leído, entendido y acordado los términos señalados en este Aviso de privacidad, por lo que otorga su consentimiento respecto al tratamiento de sus Datos Personales conforme lo establece la LFPDP y legislación aplicable. 
                1.3- Que en caso de que los Datos Personales recopilados incluyan Datos Personales sensibles o financieros, sean estos proporcionados en formato impreso, o utilizando medios electrónicos, son actos que constituyen su consentimiento expreso como su titular en términos del segundo párrafo del artículo 8 de la LFPDP y demás legislación aplicable. 
                1.4- Que otorga su consentimiento para que IA Society o sus Encargados realicen transferencias de Datos Personales a terceros nacionales o extranjeros, en el entendido de que el tratamiento que dichos terceros den a sus Datos Personales deberá ajustarse a lo establecido en este Aviso de privacidad.
                1.5- Que en caso de que el Titular no se oponga a los términos del Aviso de privacidad se considerará acordado y consentido su contenido y que el consentimiento del Titular podrá ser revocado en cualquier momento por éste.
                No obstante, cualquier disposición de este Aviso de privacidad, el Titular reconoce que no se requerirá de su consentimiento para el tratamiento de Datos Personales por parte del Responsable o de terceros en cualquiera de los casos señalados en el Artículo 10 de la LFPDP. 
                </p>
                <h2 class="section-heading">2. RECOLECCIÓN DE LOS DATOS PERSONALES.</h2>
                <p>
                La recolección de Datos Personales podrá efectuarse, por comunicación vía telefónica con el Responsable o con sus Encargados, empleados o bien, mediante entrega directa al Responsable o mediante el uso de correos electrónicos y/o mensajes cortos de texto, o mediante la utilización de sus sitios Web, mediante el uso de herramientas de captura automática de datos. Las cuales permiten recolectar la información que envía su navegador a dichos sitios Web, tales como el tipo de navegador que utiliza, el idioma de usuario, y la dirección IP de sitios Web que utilizó para acceder a los sitios del Responsable o Encargados. 
                Los siguientes son ejemplos, a título enunciativo mas no limitativo, de información que el Responsable puede recopilar: nombre y apellidos; fecha de nacimiento; domicilio, sea particular, del trabajo, o fiscal; dirección de correo electrónico, personal o del trabajo; nombre de identificación en redes sociales; número telefónico, particular o del trabajo; número de teléfono celular; número  cuentas bancarias; clave del Registro Federal de Contribuyentes (RFC); Clave Única de Registro de Población (CURP).
                Dentro de la documentación que puede ser recolectada por IA Society para la verificación de la identidad del Titular de los Datos Personales se encuentra la credencial de elector; la cartilla de identidad del servicio militar nacional liberada; la cédula profesional; pasaporte; Forma migratoria; la cédula de registro en el RFC; la cédula de registro con la CURP; el comprobante de domicilio; el informe especial de crédito emitido por una Sociedad de Información Crediticia. Para el caso de postulantes a empleados podría también solicitarse, entre otros, el acta de nacimiento; comprobantes de estudios; el estado de cuenta de la Administradora de Fondos para el retiro (Afore); la hoja de retención de impuestos, así como de los estudio psicométrico, médico y socio-económico efectuados por IA Society. También le podrían ser solicitados los Datos Personales de sus familiares, dependientes o beneficiarios y la documentación comprobatoria conducente.
                </p>
                <h2 class="section-heading">3. FINALIDAD DE LOS DATOS PERSONALES.</h2>
                <p>
                Los Datos Personales del Titular son recolectados y tratados por el Responsable o sus Encargados con la finalidad de permitirle al Titular, llevar a cabo las siguientes actividades con el Responsable:
                A) Solicitar, comprar, cambiar, o devolver productos, ofrecidos por IA Society;
                B) Solicitar, contratar, cambiar o cancelar servicios, ofrecidos por IA Society;
                C) Efectuar pagos en línea;
                D) Solicitar factura o comprobante fiscal digital;
                E) Solicitar una cotización, sobre productos y servicios;
                F) Solicitar la entrega, reparación o cumplimiento de garantía de productos;
                G) Contactar el Servicio de Atención a Clientes;
                H) Participar en encuestas;
                I) Utilizar los distintos servicios de sus correspondientes Sitios Web incluyendo la descarga de contenidos y formatos; 
                J) Compartir sus comentarios o sugerencias sobre los productos y servicios; 
                K) Procesar pagos, y
                L) Cualquier otra actividad de naturaleza análoga a las descritas en los incisos previamente citados. 
                Asimismo, el Responsable, directamente o a través de sus Encargados, puede utilizar sus Datos Personales con la finalidad de:
                A) Realizar estudios sobre intereses y comportamiento de sus clientes, consumidores, proveedores, y de aquellos terceros con los que trate;
                B) Efectuar estudios de mercado y de consumo a efecto de adquirir y ofrecer productos y servicios personalizados, así como publicidad y contenidos más adecuados a las necesidades de sus clientes, consumidores, proveedores y otros terceros con los que trate; 
                C) Elaborar estadísticas internas que indiquen los servicios y productos más apreciados por los diferentes segmentos de sus clientes, consumidores, proveedores;
                D) Formalizar el proceso transaccional con sus clientes, consumidores, proveedores y otros terceros con los que trate;
                E) Gestionar el proceso de requisición, evaluación y adjudicación de proveedores y otros terceros con los que trate;
                F) En su caso, verificar la capacidad de crédito y/o pago del Titular, lo cual se podrá efectuar a través de las sociedades de información crediticia, en términos de la Ley para regular las Sociedades de Información Crediticia; 
                G) Comprobar las transacciones comerciales realizadas por el Titular con el Responsable;
                Para el caso de los postulantes a empleados de IA Society, se llevarán a cabo las siguientes actividades:
                A) Efectuar el proceso de reclutamiento y selección de personal; 
                B) Realizar el proceso de contratación
                </p>
                <h2 class="section-heading">4. RECOLECCIÓN DE DATOS AL NAVEGAR EN SITIOS Y PÁGINAS WEB DE IA SOCIETY.</h2>
                <p> 
                Dentro de las herramientas de captura automática de datos utilizadas por IA Society en sus sitios y páginas web se encuentran las cookies, los Web, y los enlaces en los correos electrónicos.
                Vínculos en los correos electrónicos de IA Society. - Los correos electrónicos que incluyen vínculos que permiten a IA Society saber si usted activó dicho vínculo y visitó la página web de destino, pudiendo esta información ser incluida en su perfil. En caso de que usted prefiera que IA Society no recolecte información sobre su interacción con dichos vínculos, usted puede optar por modificar el formato de las comunicaciones de IA Society (por ejemplo, que el mensaje sea recibido en formato de texto y no en formato HTML) o puede hacer caso omiso del vínculo y no acceder a su contenido.
                </p>
                <h2 class="section-heading">5. TRANSFERENCIAS DE DATOS.</h2>
                <p> 
                Habiendo leído, entendido y acordado los términos expuestos en este Aviso de privacidad, el Titular manifiesta su consentimiento para que el Responsable o cualquier Encargado realicen transferencias de Datos Personales a terceros nacionales o extranjeros, en el entendido de que el tratamiento que dichos terceros den a los Datos Personales del Titular deberá ajustarse a lo establecido en este Aviso de privacidad.
                </p>
                <h2 class="section-heading">6. LIMITACIÓN DE USO Y DIVULGACIÓN DE LA INFORMACIÓN.</h2>
                <p>
                El Responsable y/o sus Encargados conservarán los Datos Personales del Titular durante el tiempo que sea necesario para procesar sus solicitudes de información, productos y/o servicios, así como para mantener los registros contables, financieros y de auditoria en términos de la LFPDP y de la legislación mercantil, fiscal y administrativa vigente. Además de controlar la divulgación de dichos datos o información de socios comerciales.
                Los Datos Personales del Titular recolectados por el Responsable y/o sus Encargados se encontrarán protegidos por medidas de seguridad administrativas, técnicas y físicas adecuadas contra el daño, pérdida, alteración, destrucción o uso, acceso o tratamiento no autorizados, de conformidad con lo dispuesto en la LFPDP y de la regulación administrativa derivada de la misma. No obstante, lo señalado anteriormente, IA Society no garantiza que terceros no autorizados no puedan tener acceso a los sistemas físicos o lógicos de los Titulares o del Responsable o en los documentos electrónicos y ficheros almacenados en sus sistemas. En consecuencia, IA Society no será en ningún caso responsable de los daños y perjuicios que pudieran derivarse de dicho acceso no autorizado.
                </p>
                <h2 class="section-heading">9. CAMBIOS AL AVISO DE PRIVACIDAD.</h2>
                <p>
                IA Society se reserva el derecho de actualizar periódicamente el presente Aviso de privacidad para reflejar los cambios en nuestras prácticas de información. Es responsabilidad del Titular revisar periódicamente el contenido del Aviso de privacidad en el sitio www.ia-society.com en donde se publicarán los cambios realizados conjuntamente con la fecha de la última actualización. El Responsable entenderá que de no expresar lo contrario, significa que el Titular ha leído, entendido y acordado los términos ahí expuestos, lo que constituye su consentimiento a los cambios y/o actualizaciones respecto al tratamiento de sus Datos Personales.
                El (Cliente, proveedor, aspirante, empleado, etc.) declara haber leído y comprendido el aviso de privacidad que en cumplimiento a lo dispuesto por la Ley de acceso a la Información y protección de datos personales, se encuentra disponible en la página electrónica http://www.ia-society.com/privacypolicy que acepta someterse al mismo respecto a el manejo de los datos que ha(n) proporcionado a IA Society S.C. y que quedan bajo su custodia, en los términos de lo señalado en dicho aviso de privacidad.
